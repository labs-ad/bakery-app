import '@testing-library/jest-dom'

// Global test setup configuration
beforeAll(() => {
  // Set test environment (read-only in some environments, so we use Object.defineProperty)
  if (!process.env.NODE_ENV) {
    Object.defineProperty(process.env, 'NODE_ENV', {
      value: 'test',
      writable: true,
      configurable: true
    })
  }
})

// Mock Next.js router
